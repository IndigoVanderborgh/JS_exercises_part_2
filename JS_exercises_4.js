// Basics of Array Methods
// pop--> remove elements to the end of the array
// push --> add elements to the end of the array
// shift --> remove elemens from the front of the array
// unshift --> add elements to the front of the array

largeCountries.pop() // removes last element from largeCountries array
largeCountries.push("Pakistan") // adds last element "Pakistan" to largeCountries array
largeCountries.shift() // removes first element from largeCountries array
largeCountries.unshift("China") // adds first element "China" to largeCountries array
